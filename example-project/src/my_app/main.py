from .print2 import print2


def hello_world():
    # print関数の利用
    print("Hello World")


def hello_world_with_print2():
    # 別名を付けたprint関数の利用
    print2("Hello World")


def single_if():
    # if文
    if True:
        pass


def double_nested_if():
    # 2重のif文
    if True:
        if True:
            pass


def triple_nested_if():
    # 3重のif文
    if True:
        if True:
            if True:
                pass


def quad_nested_if():
    # 4重のif文
    if True:
        if True:
            if True:
                if True:
                    pass
